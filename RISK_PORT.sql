USE RISK_PORTFOLIO

--==========================================================STEP 1 - #STEP1_BASE 
DROP TABLE IF EXISTS #STEP1_BASE

SELECT 
	LOAN_ACCOUNT_NUMBER
	,DISBURSEMENT_DATE
	,FIRST_PAYMENT_DATE
	,LOAN_AMOUNT
	,PRINCIPAL_OUTSTANDING
	,DPD
	,DATEDIFF(MONTH,FIRST_PAYMENT_DATE,REPORT_DATE) + 1 AS PAYMENT_MONTH_NO
INTO #STEP1_BASE
FROM RISK_PORTFOLIO..FACT_LOAN_MANAGEMENT
WHERE REPORT_DATE >= FIRST_PAYMENT_DATE 
ORDER BY REPORT_DATE


--==========================================================STEP 2 - #STEP2_MAX_DPD
DROP TABLE IF EXISTS #STEP2_MAX_DPD

SELECT 
	LOAN_ACCOUNT_NUMBER
	,DISBURSEMENT_DATE
	,FIRST_PAYMENT_DATE
	,LOAN_AMOUNT
	,PRINCIPAL_OUTSTANDING
	,PAYMENT_MONTH_NO
	,MAX(DPD) AS MAX_DPD_IN_MONTH
INTO #STEP2_MAX_DPD
FROM [#STEP1_BASE]
WHERE PAYMENT_MONTH_NO BETWEEN 1 AND 3
GROUP BY	 
		LOAN_ACCOUNT_NUMBER
		,DISBURSEMENT_DATE
		,FIRST_PAYMENT_DATE
		,LOAN_AMOUNT
		,PRINCIPAL_OUTSTANDING
		,PAYMENT_MONTH_NO


--==========================================================STEP 3 - #STEP3_FLAGS
DROP TABLE IF EXISTS #STEP3_FLAG

SELECT
	LOAN_ACCOUNT_NUMBER
	,DISBURSEMENT_DATE
	,FIRST_PAYMENT_DATE
	,LOAN_AMOUNT
	,[FPD FLAG]	=		MAX(CASE WHEN PAYMENT_MONTH_NO = 1 AND MAX_DPD_IN_MONTH > 0 THEN 1 ELSE 0 END)
	,[SPD_FLAG] =		MAX(CASE WHEN PAYMENT_MONTH_NO = 2 AND MAX_DPD_IN_MONTH > 0 THEN 1 ELSE 0 END)
	,[TPD_FLAG] =		MAX(CASE WHEN PAYMENT_MONTH_NO = 3 AND MAX_DPD_IN_MONTH > 0 THEN 1 ELSE 0 END)
	,[FPD_FLAG_PRIN] =	MAX(CASE WHEN PAYMENT_MONTH_NO = 1 AND MAX_DPD_IN_MONTH > 0 THEN PRINCIPAL_OUTSTANDING ELSE 0 END)
	,[SPD_FLAG_PRIN] =	MAX(CASE WHEN PAYMENT_MONTH_NO = 2 AND MAX_DPD_IN_MONTH > 0 THEN PRINCIPAL_OUTSTANDING ELSE 0 END)
	,[TPD_FLAG_PRIN] =	MAX(CASE WHEN PAYMENT_MONTH_NO = 3 AND MAX_DPD_IN_MONTH > 0 THEN PRINCIPAL_OUTSTANDING ELSE 0 END)
	,[MAX_DPD_M1] =		MAX(CASE WHEN PAYMENT_MONTH_NO = 1							THEN MAX_DPD_IN_MONTH ELSE 0 END)
	,[MAX_DPD_M2] =		MAX(CASE WHEN PAYMENT_MONTH_NO = 2							THEN MAX_DPD_IN_MONTH ELSE 0 END)
	,[MAX_DPD_M3] =		MAX(CASE WHEN PAYMENT_MONTH_NO = 3							THEN MAX_DPD_IN_MONTH ELSE 0 END)
INTO #STEP3_FLAG
FROM [#STEP2_MAX_DPD]
GROUP BY 
		LOAN_ACCOUNT_NUMBER
		,DISBURSEMENT_DATE
		,FIRST_PAYMENT_DATE
		,LOAN_AMOUNT

--==========================================================STEP 4 - #STEP4_DEFAULT_METRIC

DROP TABLE IF EXISTS #STEP4_DEFAULT_METRIC

SELECT
	LOAN_ACCOUNT_NUMBER
	,DISBURSEMENT_DATE
	,FIRST_PAYMENT_DATE
	,[FPD FLAG]
	,SPD_FLAG
	,TPD_FLAG
	,MAX_DPD_M1
	,MAX_DPD_M2
	,MAX_DPD_M3
	,[DEFAULT LABEL] = CASE 
							WHEN [FPD FLAG]		= 1		AND SPD_FLAG = 1 AND TPD_FLAG = 1	THEN 'FPD + SPD + TPD'
							WHEN FPD_FLAG_PRIN	= 1		AND SPD_FLAG = 1					THEN 'FPD + SPD'
							WHEN SPD_FLAG		= 1		AND TPD_FLAG = 1					THEN 'SPD + TPD'
							WHEN [FPD FLAG]		= 1		AND TPD_FLAG = 1					THEN 'FPD + TPD'
							WHEN [FPD FLAG]		= 1											THEN 'FPD Only'
							WHEN SPD_FLAG		= 1											THEN 'SPD Only'
							WHEN TPD_FLAG		= 1											THEN 'TPD Only'
							ELSE																 'No Default' END

INTO #STEP4_DEFAULT_METRIC
FROM [#STEP3_FLAG]
ORDER BY LOAN_ACCOUNT_NUMBER


